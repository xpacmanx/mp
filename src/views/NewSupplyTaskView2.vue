function getValue(obj, key) {
  const keys = key.split('.');
  let result = obj;

  for (const k of keys) {
    if (result.hasOwnProperty(k)) {
      result = result[k];
    } else {
      result = undefined;
      break;
    }
  }

  return result;
}
	
